CREATE TABLE museums (
    id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    MID INTEGER NOT NULL,
    COMMONNAME TEXT NOT NULL,
    PHONE INTEGER,
    WEBURL TEXT,
    DISCIPL TEXT NOT NULL,
    LONGITUDE INTEGER NOT NULL,
    LATITUDE INTEGER NOT NULL,
    GSTREET TEXT,
    GCITY TEXT,
    GSTATE TEXT,
    GZIP5 TEXT
);